package Basics;

public class dataTypes {
    public static void main(String[] args) {
        boolean flag = true ;
        int a = 123;
        short s = 24355;
        byte b = 120;
        long l = 7574789;
        float f = 35.423f;
        double d = 50978.0745;
        char ch = 'P' ;

        System.out.println("The boolean variable is "+flag);
        System.out.println("The integer variable : "+a);
        System.out.println("The short variable : "+s);
        System.out.println("The byte variable : "+b);
        System.out.println("The long variable : "+l);
        System.out.println("The float variable : "+f);
        System.out.println("The double variable : "+d);
        System.out.println("The character variable : "+ch);

    }
}

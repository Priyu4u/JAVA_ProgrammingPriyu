package Basics;

public class arithmeticOperations {
    public static void main(String[] args) {
        int x = 25 ;
        int y = 20 ;
        // arithmetic operators ->  + , - , * , /
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println((float)x/y);
    }
}
